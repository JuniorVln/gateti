    <script>
        (function () {
            function initTimeline() {
                var track = document.getElementById('timeline-track');
                var activeDot = document.getElementById('timeline-active-dot');
                var highlight = document.getElementById('timeline-highlight');
                if (!track || !activeDot || !highlight) return;

                var nodes = Array.from(track.querySelectorAll('.timeline-node'));
                var currentIndex = -1;

                function getOffsetInTrack(el) {
                    var top = 0;
                    var cur = el;
                    while (cur && cur !== track) {
                        top += cur.offsetTop;
                        cur = cur.offsetParent;
                    }
                    return top;
                }

                function getDotTop(node) {
                    var dot = node.querySelector('.node-dot');
                    return getOffsetInTrack(dot) + dot.offsetHeight / 2 - activeDot.offsetHeight / 2;
                }

                function setActive(index) {
                    if (index === currentIndex) return;
                    currentIndex = index;

                    var node = nodes[index];

                    // Move dot
                    activeDot.style.top = getDotTop(node) + 'px';

                    // Move highlight box
                    var nodeTop = getOffsetInTrack(node);
                    highlight.style.top = nodeTop + 'px';
                    highlight.style.height = node.offsetHeight + 'px';
                    highlight.style.opacity = '1';

                    // Label colors
                    nodes.forEach(function (n, i) {
                        var label = n.querySelector('.node-label');
                        if (label) label.style.color = i === index ? '#1B9DD9' : '';
                    });
                }

                function onScroll() {
                    var mid = window.innerHeight * 0.52;
                    var best = 0;
                    nodes.forEach(function (node, i) {
                        if (node.getBoundingClientRect().top < mid) best = i;
                    });
                    setActive(best);
                }

                function onResize() {
                    if (currentIndex >= 0) {
                        var node = nodes[currentIndex];
                        activeDot.style.top = getDotTop(node) + 'px';
                        highlight.style.top = getOffsetInTrack(node) + 'px';
                        highlight.style.height = node.offsetHeight + 'px';
                    }
                }

                window.addEventListener('scroll', onScroll, { passive: true });
                window.addEventListener('resize', onResize, { passive: true });

                // Init without transition, then enable
                setTimeout(function () {
                    activeDot.style.transition = 'none';
                    highlight.style.transition = 'none';
                    var node0 = nodes[0];
                    activeDot.style.top = getDotTop(node0) + 'px';
                    highlight.style.top = getOffsetInTrack(node0) + 'px';
                    highlight.style.height = node0.offsetHeight + 'px';
                    highlight.style.opacity = '1';
                    nodes[0].querySelector('.node-label').style.color = '#1B9DD9';
                    currentIndex = 0;
                    setTimeout(function () {
                        activeDot.style.transition = 'top 0.55s cubic-bezier(0.16,1,0.3,1)';
                        highlight.style.transition = 'top 0.55s cubic-bezier(0.16,1,0.3,1), height 0.55s cubic-bezier(0.16,1,0.3,1), opacity 0.3s ease';
                        onScroll();
                    }, 60);
                }, 250);
            }

            if (document.readyState === 'loading') {
                document.addEventListener('DOMContentLoaded', initTimeline);
            } else {
                initTimeline();
            }
        })();
    </script>

